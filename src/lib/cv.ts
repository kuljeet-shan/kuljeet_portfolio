const matchLines = (re: RegExp): string[] => {
  const out: string[] = [];
  const rx = new RegExp(re.source, re.flags.includes("g") ? re.flags : re.flags + "g");
  let m: RegExpExecArray | null;
  while ((m = rx.exec(text)) !== null) out.push(m[0].trim());
  return out;
};

export const extractSectionLines = (headingRe: RegExp): string[] => {
  if (!text) return [];
  const sectionsRe = /^(EDUCATION|EXAMS\s+QUALIFIED|EMPLOYMENT\s+HISTORY|RESEARCH\s+INTERESTS|RESEARCH\s+JOURNAL\s+PUBLICATIONS|CONFERENCE\s+(?:PUBLICATIONS|PROCEEDINGS|PAPERS)|BOOK\s+CHAPTERS|PATENTS|RESEARCH\s+AWARDS|INVITED\s+AS\s+KEYNOTE\s+SPEAKER|MEMBERSHIP\s+OF\s+PROFESSIONAL\s+BODIES|ACADEMIC\s+ACHIEVEMENTS|TECHNICAL\s+SKILLS|COURSES\s+TAUGHT|WORKSHOPS|SEMINARS|REFERENCE|PERSONAL\s+DETAILS|OBJECTIVES|EDITORIAL\s+ROLES|REVIEWER\s+ACTIVITIES|NEWSPAPER\s+ARTICLES|SEMINARS\s*\/\s*WORKSHOPS\s+ORGANIZED|CONFERENCES\s*\/\s*WORKSHOPS\s*\/\s*UGC\s+MM-TTP\s+FDP’S\s*\/\s*PROJECTS\s+ATTENDED)/im;
  
  const lines = text.split(/\n+/).map((l) => l.trim());
  let capturing = false;
  const out: string[] = [];
  
  for (let i = 0; i < lines.length; i++) {
    const l = lines[i];
    if (!capturing && headingRe.test(l)) {
      capturing = true;
      continue;
    }
    if (capturing) {
      if (sectionsRe.test(l) && !headingRe.test(l)) break;
      if (l.length) out.push(l);
    }
  }
  return out;
};

