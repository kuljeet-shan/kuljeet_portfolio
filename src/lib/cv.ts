  export const parseDevelopmentEvents = (): StructuredEvent[] => {
    const lines = extractSectionLines(/CONFERENCES\s*\/\s*WORKSHOPS\s*\/\s*UGC\s+MM-TTP\s+FDP’S\s*\/\s*PROJECTS\s+ATTENDED/i);
    const events: StructuredEvent[] = [];
    
    for (const line of lines) {
      const trimmed = line.trim();
      if (/^\d+\)/.test(trimmed)) {
        const idMatch = trimmed.match(/^(\d+)\)/);
        const id = idMatch ? idMatch[1] : "";
        const text = trimmed.replace(/^\d+\)\s*/, "");
        
        let type: StructuredEvent["type"] = "other";
        if (text.toLowerCase().includes("fdp") || text.toLowerCase().includes("programme") || text.toLowerCase().includes("induction")) type = "fdp";
        else if (text.toLowerCase().includes("conference")) type = "conference";
        else if (text.toLowerCase().includes("workshop")) type = "workshop";
        else if (text.toLowerCase().includes("project") || text.toLowerCase().includes("investigator")) type = "project";

        const cert = text.includes("[Certificate]");
        let cleanText = text.replace(/\[Certificate\]/g, "").trim();
        
        let title = cleanText;
        let subtitle = "";
        let organizer = "";
        let location = "";
        
        // Extract date
        const dateMatch = cleanText.match(/(\d{1,2}(?:st|nd|rd|th)?\s+[A-Za-z]+\s+\d{4}\s+to\s+\d{1,2}(?:st|nd|rd|th)?\s+[A-Za-z]+\s+\d{4})/i) || 
                        cleanText.match(/(\d{1,2}-\d{1,2}\s+[A-Za-z]+\s+\d{4})/i) ||
                        cleanText.match(/([A-Z][a-z]+\s+\d{1,2}-\d{1,2},\s+\d{4})/i) ||
                        cleanText.match(/(\d{1,2}(?:st|nd|rd|th)?\s+–\s+\d{1,2}(?:st|nd|rd|th)?\s+[A-Za-z]+,\s+\d{4})/i) ||
                        cleanText.match(/on\s+(\d{1,2}-\d{1,2}\s+[A-Za-z]+\s+\d{4})/i) ||
                        cleanText.match(/during\s+(\d{1,2}(?:st|nd|rd|th)?\s+-\s+\d{1,2}(?:st|nd|rd|th)?\s+[A-Z][a-z]+,\s+\d{4})/i) ||
                        cleanText.match(/held during\s+(.*?)(?:,|$)/i);

        let date = dateMatch ? dateMatch[0].replace(/^on\s+/i, "").replace(/^during\s+/i, "").replace(/^held during\s+/i, "").trim() : undefined;

        // Extract subtitle (under the... or of the...)
        const subtitleMatch = cleanText.match(/under the\s+(.*?)(?:\s+organized|\s+sponsored|\s+held|\s+on|\s+from|,|$)/i);
        if (subtitleMatch) {
          subtitle = subtitleMatch[1].trim();
        }

        // Extract organizer/location patterns
        const orgPatterns = [
          /organized by\s+(.*?)(?:\s+on|\s+from|\s+at|\s+during|,|$)/i,
          /sponsored by\s+(.*?)(?:\s+located|\s+on|,|$)/i,
          /jointly organized by\s+(.*?)(?:\s+in collaboration|\s+during|,|$)/i
        ];

        for (const pattern of orgPatterns) {
          const match = cleanText.match(pattern);
          if (match) {
            organizer = match[1].trim();
            break;
          }
        }

        const locPatterns = [
          /at\s+(.*?)(?:\s+on|\s+from|,|$)/i,
          /located in\s+(.*?)(?:\s+on|,|$)/i,
          /organized by.*?\s+at\s+(.*?)(?:\s+on|,|$)/i
        ];

        for (const pattern of locPatterns) {
          const match = cleanText.match(pattern);
          if (match) {
            location = match[1].trim();
            break;
          }
        }

        // Clean up title - remove the "Completed the" or "Participated in" prefixes
        title = title.replace(/^(Completed|Participated in|Worked as a|Organized|Participated and presented a research paper in|Participated 5 days International Research workshop on)\s+the\s+/i, "")
                     .replace(/^(Completed|Participated in|Worked as a|Organized|Participated and presented a research paper in|Participated 5 days International Research workshop on)\s+/i, "")
                     .replace(/under the.*$/i, "")
                     .replace(/organized by.*$/i, "")
                     .replace(/sponsored by.*$/i, "")
                     .replace(/held during.*$/i, "")
                     .replace(/during.*$/i, "")
                     .replace(/on\s+\d{1,2}.*$/i, "")
                     .replace(/from\s+\d{1,2}.*$/i, "")
                     .replace(/at\s+['‘].*?['’]/i, "")
                     .trim();
        
        if (title.endsWith(",") || title.endsWith(".")) title = title.slice(0, -1);
        if (title.startsWith("“") && title.endsWith("”")) title = title.slice(1, -1);

        events.push({
          id,
          title: title.trim(),
          subtitle: subtitle.trim(),
          organizer,
          location,
          type,
          certificate: cert,
          date
        });
      }
    }
    return events;
  };

