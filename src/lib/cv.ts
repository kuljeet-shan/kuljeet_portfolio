export const parseAllPublicationsStructured = (): StructuredPublication[] => {
  const rawLines = parseAllPublications();
  const structured: StructuredPublication[] = [];
  let currentPub: Partial<StructuredPublication> | null = null;

  for (const line of rawLines) {
    const trimmed = line.trim();
    if (!trimmed) continue;

    if (/^\d+\)/.test(trimmed)) {
      if (currentPub && currentPub.title) structured.push(currentPub as StructuredPublication);
      const idMatch = trimmed.match(/^(\d+)\)/);
      const id = idMatch ? idMatch[1] : undefined;
      const parts = trimmed.split(/“|”|"/);
      let authors = "";
      let title = "";
      let rest = "";

      if (parts.length >= 3) {
        authors = parts[0].replace(/^\d+\)\s*/, "").replace(/,\s*$/, "").trim();
        title = parts[1].trim();
        rest = parts.slice(2).join("").trim();
      } else {
        authors = trimmed.replace(/^\d+\)\s*/, "").split(",")[0].trim();
        title = trimmed;
      }

      const yearMatch = rest.match(/\((\d{4})\)/);
      const year = yearMatch ? yearMatch[1] : "";
      const metadataMatch = rest.match(/\[(.*?)\]/);
      const metadata = metadataMatch ? metadataMatch[1].split(",").map(m => m.trim()) : [];
      const venue = rest.replace(/\(\d{4}\)/, "").replace(/\[.*?\]/, "").replace(/ISSN:.*?\)/, "").replace(/^,\s*/, "").replace(/,\s*$/, "").trim();

      currentPub = { id, authors, title, venue, year, metadata };
    } else if (currentPub) {
      if (trimmed.startsWith("[")) {
        const metadataMatch = trimmed.match(/\[(.*?)\]/);
        if (metadataMatch) currentPub.metadata = [...(currentPub.metadata || []), ...metadataMatch[1].split(",").map(m => m.trim())];
      } else if (trimmed.includes("http")) {
        const doiMatch = trimmed.match(/https?:\/\/[^\s]+/);
        if (doiMatch) currentPub.doi = doiMatch[0];
      } else {
        if (currentPub.venue) currentPub.venue += " " + trimmed;
        else currentPub.title += " " + trimmed;
      }
    }
  }
  if (currentPub && currentPub.title) structured.push(currentPub as StructuredPublication);
  return structured;
};

